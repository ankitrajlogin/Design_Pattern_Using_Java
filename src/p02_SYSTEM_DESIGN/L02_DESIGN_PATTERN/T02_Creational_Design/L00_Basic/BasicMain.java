/*
Creational Design Patterns
- Creational design patterns deal with object creation mechanisms. Instead of creating objects directly using the new keyword everywhere, these patterns give us flexible and reusable ways to create objects.

💡 Problem: If we hardcode new everywhere, the code becomes tightly coupled, less reusable, and harder to extend.
💡 Solution: Creational patterns abstract how objects are created, so the system can be more scalable, reusable, and maintainable.
 */

/*
🔹 Why do we use them? (Importance)
 - Encapsulation of object creation → Client code does not need to know the exact class name.
 - Reduce coupling → Objects are created in a centralized way.
 - Flexibility → Easily swap, extend, or change object creation logic.
 - Control → Can enforce rules on how and when objects are created (e.g., singleton).
 */


/*
Types of Creational Patterns
1. Singleton → Only one instance of a class should exist.
2. Factory Method → A superclass defines a method for creating objects, but subclasses decide which object to create.
3. Abstract Factory → Provides an interface for creating families of related objects without specifying exact classes.
4. Builder → Step-by-step construction of complex objects.
5. Prototype → Clone objects instead of creating new ones from scratch.
 */


